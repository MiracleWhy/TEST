package com.uton.carsokApi.model;

public class ChildRoleVo {
	private int id;
	private String child_account_mobile;
	private String child_account_name;
	private String roleName;
	private String alias;
	public String getChild_account_mobile() {
		return child_account_mobile;
	}
	public void setChild_account_mobile(String child_account_mobile) {
		this.child_account_mobile = child_account_mobile;
	}
	public String getChild_account_name() {
		return child_account_name;
	}
	public void setChild_account_name(String child_account_name) {
		this.child_account_name = child_account_name;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	
	
}
